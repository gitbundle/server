package action

import (
	"time"
)

// Actioner describes an action
type Actioner interface {
	GetOpType() ActionType
	GetActUserName() string
	GetRepoUserName() string
	GetRepoName() string
	GetRepoPath() string
	GetRepoLink() string
	GetBranch() string
	GetContent() string
	GetCreate() time.Time
	GetIssueInfos() []string
}

// ActionType represents the type of an action.
type ActionType int
